    private void jButton_RegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_RegisterActionPerformed
        // TODO add your handling code here:
        String fname=jTextField_fullname.getText();
        String uname=jTextField_username.getText();
        String pass1=String.valueOf(jPasswordField_password.getPassword())  ;
        String pass2=String.valueOf(jPasswordField_confirmpassword.getPassword())  ;
        String phone=jTextField_phone.getText();
         String gender="Male";
         
         if(jRadioButton_female.isSelected()){
            gender="Female";
          }
         if(verifyfield()){
             
             if(!checkusername(uname)){
                 PreparedStatement st;
                 ResultSet rs;
                
      
                 String registeruserquery="INSERT INTO `users`( `fullname`, `username`, `password`, `phone`, `gender`, `image`) VALUES (?,?,?,?,?,?)";
                  try {
            
                  st=(PreparedStatement) My_CON.getConnection().prepareStatement(registeruserquery);
                  st.setString(1,fname);
                  st.setString(2,uname);
                  st.setString(3,pass1);
                  st.setString(4,phone);
                  st.setString(5,gender);
                  try{
                      
                      if(image_path!=null){
                        InputStream image=new FileInputStream(new File(image_path));
                        st.setBlob(6,image);
                      }
                       else{
                             st.setNull(6, java.sql.Types.NULL);
                         }
                     
                     if(st.executeUpdate()!=0){
                       JOptionPane.showMessageDialog(null, "Your acccount has been added");
                     }else{
                       JOptionPane.showMessageDialog(null, "Your acccount has been not added");
                     }
                  }catch(FileNotFoundException ex){
                        Logger.getLogger(Register_Form.class.getName()).log(Level.SEVERE, null, ex);
                  }
                  
            
                  } catch (SQLException ex) {
                    Logger.getLogger(Register_Form.class.getName()).log(Level.SEVERE, null, ex);
                  }
      
             }
         }
    
    }//GEN-LAST:event_jButton_RegisterActionPerformed


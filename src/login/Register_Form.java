    //create a function to verify empty field
    public boolean verifyfield(){
        String fname=jTextField_fullname.getText();
        String uname=jTextField_username.getText();
        String phone=jTextField_phone.getText();
        String pass1=String.valueOf(jPasswordField_password.getPassword());
        String pass2=String.valueOf(jPasswordField_confirmpassword.getPassword());
        
        if(fname.trim().equals("") || uname.trim().equals("") || phone.trim().equals("") ||
                pass1.trim().equals("") || pass2.trim().equals("")){
            JOptionPane.showMessageDialog(null, "one more field are empty","Empty field ",2);
            return false;
        }
        else if(!pass1.equals(pass2)){
                    JOptionPane.showMessageDialog(null, "password are not matched","Confirm Password",2);
                    return false;
        }
        
        else{
          return true;
        }
    }
  
    //create a function  to check if the entered username is already register or not  in the database
    public boolean checkusername(String username) {
      PreparedStatement st;
      ResultSet rs;
      boolean username_exit=false;
      
      String query="SELECT * FROM `users` WHERE `username`=?";
      
      
           try {
            
             st=(PreparedStatement) My_CON.getConnection().prepareStatement(query);
      st.setString(1,username);
      rs=st.executeQuery();
      if(rs.next()){
         username_exit=true;
         JOptionPane.showMessageDialog(null, "This username already taken","Choose another one",2);
         
      }
            
        } catch (SQLException ex) {
            Logger.getLogger(Register_Form.class.getName()).log(Level.SEVERE, null, ex);
        }
        
      
     
      return username_exit;
    }
